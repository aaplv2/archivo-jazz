export function getSongBySlug(slug) {
  return songsData.find((song) => song.id === slug);
}

export function getAllSongs() {
  return songsData.sort((a, b) => a.title.localeCompare(b.title));
}

export function getSongsByDecade(decade) {
  return songsData.filter((song) => song.decade === decade);
}
